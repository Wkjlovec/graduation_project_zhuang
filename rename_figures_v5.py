def _apply_font(run):
    run.font.size = Pt(10.5)
    run.font.name = FONT_TNR
    rPr = run._element.find(qn("w:rPr"))
    if rPr is None:
        rPr = run._element.makeelement(qn("w:rPr"), {})
        run._element.insert(0, rPr)
    rFonts = rPr.find(qn("w:rFonts"))
    if rFonts is None:
        rFonts = rPr.makeelement(qn("w:rFonts"), {})
        rPr.insert(0, rFonts)
    rFonts.set(qn("w:ascii"), FONT_TNR)
    rFonts.set(qn("w:hAnsi"), FONT_TNR)
    rFonts.set(qn("w:eastAsia"), FONT_SONG)


def main():
    doc_path = "docs/论文修改.docx"
    doc = Document(doc_path)

    count = 0
    for i, p in enumerate(doc.paragraphs):
        t = p.text.strip()
        if t in RENAMES:
            new_name = RENAMES[t]
            p.clear()
            run = p.add_run(new_name)
            _apply_font(run)
            count += 1
            print(f"  [{i:4d}] → {new_name}")

    doc.save(doc_path)
    print(f"\n完成：{count} 处标题已改写为简短版本。")


if __name__ == "__main__":
    main()
